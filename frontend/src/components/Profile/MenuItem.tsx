import * as React from "react";
import { motion } from "framer-motion";

const variants = {
  open: {
    y: 0,
    opacity: 1,
    transition: {
      y: { stiffness: 1000, velocity: -100 }
    }
  },
  closed: {
    y: 50,
    opacity: 0,
    transition: {
      y: { stiffness: 1000 }
    }
  }
};

const colors = ["#f44242", "#ea0e0e", "#e21313"];

export const MenuItem = ({ i, msg }:any) => {
  const style = { border: `0.8mm ridge ${colors[i]}` };
  return (
    <motion.li
      variants={variants}
      whileHover={{ scale: 1.1 }}
      whileTap={{ scale: 0.95 }}
    >
      <div className="icon-placeholder" style={style}></div>
      <div className="text-placeholder" style={style}>{msg}</div>
    </motion.li>
  );
};